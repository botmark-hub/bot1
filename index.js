    rawHeaders2: headerRow2            // ‡∏™‡πà‡∏á‡∏Å‡∏•‡∏±‡∏ö header ‡∏ä‡∏±‡πâ‡∏ô‡∏ó‡∏µ‡πà 2 ‡πÄ‡∏ú‡∏∑‡πà‡∏≠‡πÉ‡∏ä‡πâ‡∏á‡∏≤‡∏ô‡∏ï‡πà‡∏≠
